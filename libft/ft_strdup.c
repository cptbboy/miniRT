#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*src;
	char	*dst;
	int		i;

	src = (char *)s;
	dst = malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!dst)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
